<h2>1. Jak mogę zarejestrować się na stronie i założyć konto użytkownika?</h2>
<p> 
	Aby zarejestrować się na stronie i założyć konto użytkownika należy kliknąć w przycisk "rejestracja" znajdujący się w prawym górnym rogu strony. Po wypełnieniu formularza rejestracyjnego należy kliknąć w przycisk "Zarejestruj". Po pomyślnej rejestracji zostanie wyświetlony komunikat informujący o pomyślnej rejestracji. Następnie należy kliknąć w przycisk "Zaloguj się" i zalogować się na swoje konto.
</p>
<h2> 2. Jak mogę znaleźć informacje na temat konkretnych produktów kosmetycznych? </h2>
<p> Wszystkie produkty znajdujące się w naszej bazie danych są dostępne w sekcji "Baza produktów". Po kliknięciu na nazwę lub zdjęcie interesującego nas produktu wyświetlą się jego wszystkie szczegóły.</p>
<h2> 3. Czy strona oferuje poradniki dotyczące pielęgnacji skóry, włosów i makijażu? </h2>
<p> Strona oferuje poradniki dotyczące pielęgnacji włosów. Znajdują się one w sekcji "Baza wiedzy". Ponadto w sekcji "Aktualności" znajdują się wpisy informujące o nowościach kosmetycznych i aktualnych trendach. </p>
